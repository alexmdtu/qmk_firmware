layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
        case _QWERTY:
            set_led_off;
            break;
        case _WIN:
            set_led_red;
            break;
        case _FN:
            set_led_green;
            break;
        default:
            break;
    }
  return state;
}